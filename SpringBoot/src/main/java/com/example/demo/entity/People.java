package com.example.demo.entity;

public class People {
    private Integer peopleId;
    private String peopleListId;
    private String peopleName;
    private String peoplePhoneNumber;
    private String peoplePrize;

    public Integer getPeopleId() {
        return peopleId;
    }

    public void setPeopleId(Integer peopleId) {
        this.peopleId = peopleId;
    }

    public String getPeopleListId() {
        return peopleListId;
    }

    public void setPeopleListId(String peopleListId) {
        this.peopleListId = peopleListId;
    }

    public String getPeopleName() {
        return peopleName;
    }

    public void setPeopleName(String peopleName) {
        this.peopleName = peopleName;
    }

    public String getPeoplePhoneNumber() {
        return peoplePhoneNumber;
    }

    public void setPeoplePhoneNumber(String peoplePhoneNumber) {
        this.peoplePhoneNumber = peoplePhoneNumber;
    }

    public String getPeoplePrize() {
        return peoplePrize;
    }

    public void setPeoplePrize(String peoplePrize) {
        this.peoplePrize = peoplePrize;
    }
}
